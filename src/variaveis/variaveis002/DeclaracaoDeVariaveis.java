public class DeclaracaoDeVariaveis {
    public static void main(String[] args) {
    int idade = 12;
    double volumeCubo = 300.190100002;
    float arestaDoTriangulo = 13.542999999F;
    short salario = 32000;
    long quantidadeDeGraosDeAreia = 99999999999999999L;
    boolean possuiCartao = true;
    String nome = "Roberto";
    char sexo = 'm';

    System.out.println("Exibindo variáveis: ");
    System.out.println(idade);
    System.out.println(volumeCubo);
    System.out.println(arestaDoTriangulo);
    System.out.println(salario);
    System.out.println(quantidadeDeGraosDeAreia);
    System.out.println(possuiCartao);
    System.out.println(nome);
    System.out.println(sexo);
    }
}
